import mongoose from "mongoose";

const eventCategorySchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      unique: true,
    },
    description: {
      type: String,
    },
    isActive: {
      type: Boolean,
      default: true,
    },
  },
  { timestamps: true }
);

export const EventCategory = mongoose.model("EventCategory", eventCategorySchema);
